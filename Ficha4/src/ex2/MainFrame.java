    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String ip = this.ipTF.getText();
        String porto = this.portoTF.getText();

        boolean verificaPorto = true;

        try {

            Integer.valueOf(porto);
        } catch (NumberFormatException e) {
            verificaPorto = false;
        }

        if (ip.isEmpty() || porto.isEmpty()) {
            mostraDialog("Tem que introduzir o IP e o porto!");
        } else {
            if (verificaPorto == true) {
                if (socket == null || socket.isClosed()) {
                    try {
                        this.socket = new Socket(ip, Integer.parseInt(porto));
                        mostraDialog("Cliente entrou com sucesso na sessão!");
                        this.output = new DataOutputStream(socket.getOutputStream());
                        this.input = new DataInputStream(socket.getInputStream());
                    } catch (Exception e) {
                    }
                } else {
                    mostraDialog("Já está numa sessão, saia com o comando end!");
                }
            } else {
                mostraDialog("O porto tem que ser um número inteiro!");
            }
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        String comando = this.comandosClienteTF.getText();
        if (socket == null || socket.isClosed()) {
            mostraDialog("Tem que iniciar sessão primeiro!");
        } else {
            if (comando.isEmpty()) {
                mostraDialog("Tem que inserir um comando!");
            } else {
                verificaComando(comando);
            }
    }//GEN-LAST:event_btnEnviarActionPerformed
    }

