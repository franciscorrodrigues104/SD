    public void verificaComando(String comando) {

        try {

            switch (comando) {

                case "like":
                    if (fezLikeDislike == false) {
                        System.out.println("faz like");
                        citacaoTA.setText("");
                        output.writeUTF("like");
                        mostraResposta();
                        mostraDialog("Like bem sucedido!");
                        fezLikeDislike = true;
                        fezCancel = false;
                    } else {
                        mostraDialog("Já fez like ou dislike, insira cancel para poder fazer novamente!");
                    }
                    break;
                case "dislike":
                    if (fezLikeDislike == false) {
                        System.out.println("dislike");
                        citacaoTA.setText("");
                        output.writeUTF("dislike");
                        mostraResposta();
                        mostraDialog("Dislike bem sucedido!");
                        fezLikeDislike = true;
                        fezCancel = false;
                    } else {
                        mostraDialog("Já fez like ou dislike, insira cancel para poder fazer novamente!");
                    }
                    break;
                case "cancel":
                    if (fezCancel == false) {
                        fezLikeDislike = false;
                        output.writeUTF("cancel");
                        citacaoTA.setText("");
                        mostraResposta();
                        fezLikeDislike = false;
                        fezCancel = true;
                    } else {
                        mostraDialog("Não pode fazer cancel duas vezes ou antes de fazer like/dislike!");
                    }
                    break;
                case "end":
                    output.writeUTF("end");
                    mostraDialog("Cliente saiu da sessão!");
                    citacaoTA.setText("");
                    comandosClienteTF.setText("");
                    socket.close();
                    mostraCitacao = false;
                    break;
                default:
                    mostraDialog("invalid command");
            }
        } catch (IOException ex) {
            System.getLogger(MainFrame.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }

    }

